fun main(args:Array<String>){
    var ch = 'A'
    when(ch){
        'A' -> println("A is a vowel")
        'E' -> println("E is a vowel")
        'I' -> println("I is a vowel")
        'O' -> println("Ois a vowel")
        'U' -> println("U is a vowel")
        else -> println("$ch is not a vowel")
    }
}